export const extractMentions = (text) => {
  const regex = /@(\w+)/g;
  let match;
  const usernames = [];

  while ((match = regex.exec(text)) !== null) {
    usernames.push(match[1]); // "Bob", "Carol"
  }

  return usernames;
};
